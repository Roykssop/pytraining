# Operaciones numericas
num1 = 10
num2 = 5
suma = num1 + num2
resta = num1 - num2
multiplicacion = num1 * num2
division = num1 / num2
resto = num1 % num2 

print("suma: {}, resta:{}, multiplicacion:{}, division: {}, resto: {} ".format(suma, resta,multiplicacion,division,resto))

