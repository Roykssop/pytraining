numero1 = int(input("Ingrese un número por favor: "))
numero2 = int(input("Ingrese otro número por favor: "))

print("######## OPERACIONES BÁSICAS ##########")
print(f"{numero1} + {numero2} = { numero1 + numero2 }")
print(f"{numero1} - {numero2} = { numero1 - numero2 }")
print(f"{numero1} x {numero2} = { numero1 * numero2 }")
print(f"{numero1} / {numero2} = { numero1 / numero2 }")